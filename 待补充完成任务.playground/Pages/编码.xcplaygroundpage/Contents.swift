 * 最早的计算机在设计时采用8个比特(bit)作为一个字节(bypte)，所以，一个字节能表示的最大的整数就是255(11111111), 0-255被用来表示大小写英文字母、数字和一些符号，这个编码表被称为ASCII码;比如大写字母A的编码是65，小写z的编码是122
 
