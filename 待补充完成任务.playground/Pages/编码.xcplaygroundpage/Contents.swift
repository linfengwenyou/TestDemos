 * 如果要表示中文，显然一个字节是不够的，至少需要两个字节，而且还不能和ASCII编码冲突，所以，中国制定了GB2312编码，用来把中文编进去; 类似，日文和韩文等其他语言也有这个问题。为了统一所有文字的编码，Unicode应运w而生。Unicode吧所有语言都统一到一套编码里，遮掩高就不会再有乱码问题了。
 
